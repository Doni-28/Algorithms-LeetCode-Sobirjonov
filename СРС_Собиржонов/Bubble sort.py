# Алгоритм пузырьковой сортировки

def bubble_sort(arr):
    """
    Функция выполняет сортировку массива методом пузырька.
    Параметры:
    arr — список чисел, который необходимо отсортировать
    Возвращает:
    отсортированный список
    """

    # получаем длину массива
    n = len(arr)

    # внешний цикл проходит по всем элементам массива
    for i in range(n):

        # внутренний цикл выполняет сравнение соседних элементов
        # после каждой итерации самый большой элемент "всплывает" в конец массива
        for j in range(0, n - i - 1):

            # если текущий элемент больше следующего
            if arr[j] > arr[j + 1]:

                # выполняем обмен элементов местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]

    # возвращаем отсортированный массив
    return arr


# пример использования алгоритма
if __name__ == "__main__":

    # исходный массив
    data = [64, 34, 25, 12, 22, 11, 90]

    print("Исходный массив:")
    print(data)

    # сортировка массива
    sorted_data = bubble_sort(data)

    print("Отсортированный массив:")
    print(sorted_data)