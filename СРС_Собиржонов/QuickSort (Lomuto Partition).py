# Функция разбиения Lomuto

def partition(arr, low, high):
    """
    Функция выполняет разбиение массива вокруг опорного элемента (pivot).
    Параметры:
    arr — массив
    low — начальный индекс
    high — конечный индекс
    Возвращает:
    индекс позиции опорного элемента после разбиения
    """

    # выбираем последний элемент в качестве опорного
    pivot = arr[high]

    # индекс меньшего элемента
    i = low - 1

    # проходим по массиву
    for j in range(low, high):

        # если текущий элемент меньше или равен опорному
        if arr[j] <= pivot:

            # увеличиваем индекс меньшего элемента
            i += 1

            # меняем элементы местами
            arr[i], arr[j] = arr[j], arr[i]

    # помещаем опорный элемент в правильную позицию
    arr[i + 1], arr[high] = arr[high], arr[i + 1]

    # возвращаем индекс опорного элемента
    return i + 1


# Алгоритм быстрой сортировки

def quick_sort(arr, low, high):
    """
    Рекурсивная функция реализации алгоритма QuickSort.

    Параметры:
    arr — массив
    low — начальный индекс
    high — конечный индекс
    """

    # рекурсия выполняется пока в подмассиве больше одного элемента
    if low < high:

        # выполняем разбиение массива
        pivot_index = partition(arr, low, high)

        # рекурсивно сортируем левую часть массива
        quick_sort(arr, low, pivot_index - 1)

        # рекурсивно сортируем правую часть массива
        quick_sort(arr, pivot_index + 1, high)


# пример использования алгоритма
if __name__ == "__main__":

    # исходный массив
    data = [10, 7, 8, 9, 1, 5]

    print("Исходный массив:")
    print(data)

    # вызов быстрой сортировки
    quick_sort(data, 0, len(data) - 1)

    print("Отсортированный массив:")
    print(data)